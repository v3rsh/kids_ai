#!/bin/bash

# Скрипт для запуска очистки тестовых данных в Docker-контейнере

echo "Запуск очистки тестовых данных..."

# Запускаем скрипт очистки внутри контейнера
docker exec random_coffee_bot python3 scripts/clean_test_data.py

# Проверяем код возврата
if [ $? -eq 0 ]; then
    echo "Очистка данных успешно завершена!"
else
    echo "Ошибка при очистке данных. Проверьте логи."
    exit 1
fi 